/*
  Contains a set of constants that go along with the Rescue hash function.
  For more information of how they were generated, please see the file rescue_constants.py.
*/
struct RescueConstants {
 public:
  static constexpr size_t kStateSize = 12;
  static constexpr size_t kNumRounds = 10;
  static constexpr size_t kBatchHeight = 32;

  using VectorT = std::array<BaseFieldElement, kStateSize>;
  using MatrixT = std::array<VectorT, kStateSize>;

  /*
    The MDS matrix for the linear transformation step.
  */
  const MatrixT k_mds_matrix;

  /*
    The inverse of k_mds_matrix.
  */
  const MatrixT k_mds_matrix_inverse;

  /*
    The round constants. Each round has two parts in which a constant vector is added to the state,
    and there is one extra addition before the first round. Therefore, k_round_constants is an array
    of length (2 * kNumRounds + 1). These are the original round constants that define the function,
    the periodic columns of the AIR are based on these constants with a few changes (see
    documentation in the AIR implementation).
  */
  const std::array<VectorT, 2 * kNumRounds + 1> k_round_constants;
};

